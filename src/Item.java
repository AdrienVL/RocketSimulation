public class Item {
    private String name;
    private int weight;

    //Setter
    public void setItem(String name, int weight){
        this.name = name;
        this.weight = weight;
           }


    // Getter
    public String getItemName() {
        return name;
    }

    // Getter
    public int getItemWeight() {
        return weight;
    }

}